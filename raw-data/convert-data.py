korok_carry_coords = [[better_round(x),better_round(y),better_round(z)] for [x,y,z] in Korok_COORDINATES_CARRY]
def find_hash(x,y,z, id):
    [a,b,c] = [better_round(x),better_round(-y),better_round(z)]
    if(id<=99):
        index=korok_carry_coords.index([a,c+105,b])
        return Korok_CARRY[index]
    else:
        index=korok_found_coords.index([a,c+105,b])
        return Korok_HIDDEN[index]

    print(f'Could not find hash for {x} {y} {z}')


